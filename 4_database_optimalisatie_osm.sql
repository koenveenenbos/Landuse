---- In dit script wordt de OSM database geoptimaliseerd. 
-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------


-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------
---- Index aanmaken over de geometrie 
-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------

DROP INDEX IF EXISTS planet_osm_polygon_way;
CREATE INDEX planet_osm_polygon_geom ON planet_osm_polygon USING GIST(way);


-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------
---- Index aanmaken over de kolom sport
-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------

DROP INDEX IF EXISTS planet_osm_polygon_sport;
CREATE INDEX planet_osm_polygon_sport ON planet_osm_polygon USING BTREE(sport);


-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------
---- Index aanmaken over de kolom leisure (vrije tijd)
-------------------------------------------------------------------------------------------------------------------
-------------------------------------------------------------------------------------------------------------------

DROP INDEX IF EXISTS planet_osm_polygon_leisure;
CREATE INDEX planet_osm_polygon_leisure ON planet_osm_polygon USING BTREE(leisure);


